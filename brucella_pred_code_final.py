# Cell 6: Data Balancing
def balance_dataset(X, y):
    """Balance the dataset using SMOTE"""
    print("âš–ï¸ Balancing dataset...")

    class_counts = Counter(y)
    minority_class = min(class_counts, key=class_counts.get)
    n_minority = class_counts[minority_class]

    print(f"ğŸ“Š Class distribution before balancing: {class_counts}")

    if n_minority > 1:
        k = max(1, min(5, n_minority - 1))
        smote = SMOTE(random_state=42, k_neighbors=k)
        X_balanced, y_balanced = smote.fit_resample(X, y)
        print(f"âœ… Used SMOTE with k_neighbors={k}")
    else:
        ros = RandomOverSampler(sampling_strategy='auto', random_state=42)
        X_balanced, y_balanced = ros.fit_resample(X, y)
        print("âœ… Used RandomOverSampler")

    print(f"ğŸ“Š Class distribution after balancing: {Counter(y_balanced)}")

    return X_balanced, y_balanced

